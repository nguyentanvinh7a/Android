package com.example.myfirstproject

fun main(args: Array<String>) {
    var number1:Double = 12.5
    var number2:Double = 3.5
    var result:Double = 0.0

    // Addition
    result = number1 + number2
    println("number1 + number2 = $result")

    // Subtraction
    result = number1 - number2
    println("number1 - number2 = $result")

    // Multiplication
    result = number1 * number2
    println("number1 * number2 = $result")

    // Division
    result = number1 / number2
    println("number1 / number2 = $result")

    // Modulus
    result = number1 % number2
    println("number1 % number2 = $result")
}